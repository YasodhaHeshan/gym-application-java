    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
      
       try {
        String username = txt_username.getText();
        String f_name = txt_firstname.getText();
        String l_name = txt_lastname.getText();
        String password = txt_password.getText();
        int tno = Integer.parseInt(txt_telNo.getText());
        int age = Integer.parseInt(txt_txtAge.getText());
        String diet_prefer = (String) cmb_dietpreferences.getSelectedItem();
        String diet_restrict = (String) cmb_dietrestrion.getSelectedItem();

        // Check for empty fields (you can customize these checks)
        if (username.isEmpty() || f_name.isEmpty() || l_name.isEmpty() || password.isEmpty() || diet_prefer.isEmpty() || diet_restrict.isEmpty()) {
            // Handle the case of empty fields, e.g., display an error message
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
        } else {
            // All data is valid, proceed with the signup process
            C_SignUp c1 = new C_SignUp();
            c1.signUpUser(username, f_name, l_name, password, tno, age, diet_prefer, diet_restrict);
        }
    } catch (NumberFormatException e) {
        // Handle the case where parsing to an integer fails (e.g., non-numeric input)
        JOptionPane.showMessageDialog(this, "Please check all the fields again.");
    }
    }//GEN-LAST:event_btn_saveActionPerformed

