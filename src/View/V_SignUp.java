    private void validateUsername() {
        String username = txt_username.getText();
        if (username.isEmpty()) {
            lbl_username_error.setText("Username cannot be empty.");
        } else if (!username.matches("^[a-zA-Z]+$")) {
            lbl_username_error.setText("Username cannot contain numbers or special characters.");
        } else {
            lbl_username_error.setText(""); // Clear error message if validation passes
        }
    }
    private void validateFirstName() {
    String firstName = txt_firstname.getText();
    if (firstName.isEmpty()) {
        lbl_fnameerror.setText("First name cannot be empty.");
    }
    else if (!firstName.matches("^[a-zA-Z]+$")) {
            lbl_fnameerror.setText("First Name cannot contain numbers or special characters.");}
    else {
        lbl_fnameerror.setText("");
    }
}
private void validateLastName() {
    String lastName = txt_lastname.getText();
    if (lastName.isEmpty()) {
        lbl_lnameerror.setText("Last name cannot be empty.");
    } 
    else if (!lastName.matches("^[a-zA-Z]+$")) {
            lbl_lnameerror.setText("Last Name cannot contain numbers or special characters.");}
    else {
        lbl_lnameerror.setText("");
    }
}
private void validatePassword() {
    String password = txt_password.getText();
    if (password.isEmpty()) {
        lbl_passworderror.setText("Password cannot be empty.");
    } else if (password.length() < 6) {
        lbl_passworderror.setText("Password must be at least 6 characters long.");
    } else {
        lbl_passworderror.setText(""); // Clear error message if validation passes
    }
}
private void validateTelNo() {
    String telNoText = txt_telNo.getText();
    if (telNoText.isEmpty()) {
        lbl_tnoerror.setText("Telephone number cannot be empty.");
    } else {
        try {
            int telNo = Integer.parseInt(telNoText);
            if (telNo < 0) {
                lbl_tnoerror.setText("Telephone number cannot be negative.");
            } else if (telNoText.length() != 10) {
                lbl_tnoerror.setText("Telephone number must have exactly 10 digits.");
            } else {
                lbl_tnoerror.setText("");
            }
        } catch (NumberFormatException e) {
            lbl_tnoerror.setText("Invalid input for telephone number.");
        }
    }
}
private void validateAge() {
    String ageText = txt_txtAge.getText();
    if (ageText.isEmpty()) {
        lbl_ageerror.setText("Age cannot be empty.");
    } else {
        try {
            int age = Integer.parseInt(ageText);
            if (age < 0) {
                lbl_ageerror.setText("Age cannot be negative.");
            } else {
                lbl_ageerror.setText("");
            }
        } catch (NumberFormatException e) {
            lbl_ageerror.setText("Invalid input for age.");
        }
    }
}
